Obstacle::Obstacle(Vector2 pos)
{
    position = pos;

    for (unsigned int row = 0; row < grid.size(); row++)
    {
        for (unsigned int col = 0; col < grid[0].size(); col++)
        {
            if (grid[row][col] == 1)
            {
                float x = position.x + col * 3;
                float y = position.y + row * 3;
                blocks.push_back(Block({x, y}));
            }
        }
    }
}

void Obstacle::Draw()
{
    for (Block block : blocks)
    {
        block.Draw();
    }
}
